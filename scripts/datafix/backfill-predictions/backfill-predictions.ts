// This branch exists only to carry this one script (per scripts/datafix/AGENTS.md's
// Delete On Merge lifecycle) — it isn't meant to be merged into main.
//
// Backfills historical predictions (Australia/Canada/China/Japan/Miami — spreadsheet
// era, before the app existed) into leagues/{leagueId}/races/{raceId}/predictions.json
// directly. Source payloads live in ./data/payloads/*.payloads.json — pre-generated by
// a separate, unrelated pipeline (extract spreadsheet -> resolve names against real
// user/racer/team ids -> emit PredictionMutation-shaped payloads) and committed here as
// static input data, same as apply-corrected-race-keys' ./data folder. Each payload was
// already validated against PredictionMutationSchema (@/lib/schemas) at generation
// time; this script re-validates on load rather than trusting that blindly.
//
// Scope: writes ONLY predictions.json. It does NOT regrade — main's only existing path
// to grade a race (PredictionService, via /api/races/{raceId}/recalculate) is a
// separate, standalone concern out of scope for a one-off backfill. After a real run,
// regrade every race this script touched via that endpoint.
//
// ./data/payloads/*.payload-issues.json and PAYLOAD_SUMMARY.md are provenance only
// (not read by this script) — they document known anomalies in the source spreadsheet
// data (duplicate driver picks, partial prop-pick submissions, empty predictions) that
// were deliberately carried through as-extracted rather than silently corrected.
const DATAFIX_ID = "backfill-predictions";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PAYLOADS_DIR = path.join(__dirname, "data", "payloads");

// 2026 season motorsport — every payload file in ./data/payloads belongs to this
// motorsport today, same as apply-corrected-race-keys.
const MOTORSPORT_ID = "9ff98309-13ac-4dd6-85db-a9afba01179f";

interface RacePayloads {
  title: string;
  raceId: string;
  leagueId: string;
  payloads: PredictionMutation[];
}

function loadAllPayloads(): RacePayloads[] {
  return readdirSync(PAYLOADS_DIR)
    .filter((f) => f.endsWith(".payloads.json"))
    .map((f) => {
      const title = f.replace(/\.payloads\.json$/, "");
      const raw = JSON.parse(readFileSync(path.join(PAYLOADS_DIR, f), "utf-8"));
      const payloads = z.array(PredictionMutationSchema).parse(raw);
      if (payloads.length === 0) throw new Error(`${f}: empty payload file`);
      return { title, raceId: payloads[0].raceId, leagueId: payloads[0].leagueId, payloads };
    });
}

function arraysEqual(a: readonly string[], b: readonly string[]): boolean {
  return a.length === b.length && a.every((v, i) => v === b[i]);
}

function propPicksEqual(a: Readonly<Partial<Record<string, string>>> | undefined, b: Record<string, string> | undefined): boolean {
  const ak = Object.keys(a ?? {}).sort();
  const bk = Object.keys(b ?? {}).sort();
  if (ak.length !== bk.length || ak.some((k, i) => k !== bk[i])) return false;
  return ak.every((k) => (a as Record<string, string>)[k] === (b as Record<string, string>)[k]);
}

// Mirrors the real predictions.json persistence shape used by
// server/repositories/blob/BlobRacePredictionBookRepository.ts (predictionsToPersistence /
// PredictionsPersistenceSchema there) — not exported from that file, so duplicated here
// rather than importing a repository into a datafix write path.
const PropPicksPersistenceSchema = z.object({
  driverOfDay: z.string().optional(),
  lapsLed: z.string().optional(),
  fastestPitStop: z.string().optional(),
  fastestLap: z.string().optional(),
  overAchiever: z.string().optional(),
  underAchiever: z.string().optional(),
  wrecker: z.string().optional(),
});
const PredictionsPersistenceSchema = z.object({
  predictions: z.record(z.string(), z.array(z.string().uuid())),
  submittedAt: z.record(z.string(), z.string()).optional(),
  submittedBy: z.record(z.string(), z.string()).optional(),
  propPicks: z.record(z.string(), PropPicksPersistenceSchema),
});
type PredictionsPersistence = z.infer<typeof PredictionsPersistenceSchema>;

const EMPTY_PREDICTIONS: PredictionsPersistence = {
  predictions: {},
  submittedAt: {},
  submittedBy: {},
  propPicks: {},
};

// Minimal, read-only lookup shapes — only the fields this script actually consumes,
// not the full persistence schema each repository owns. LeagueSchema/RacerSchema in
// @/lib/schemas don't carry memberIds/id-only shape needed here, and pulling in a
// repository class would put one in the write path's dependency graph.
const LeagueLookupSchema = z.object({
  id: z.string().uuid(),
  memberIds: z.array(z.string()).default([]),
}).passthrough();
const TeamLookupSchema = z.object({
  id: z.string().uuid(),
  memberIds: z.array(z.string()).default([]),
}).passthrough();
const RacerLookupSchema = z.object({
  id: z.string().uuid(),
}).passthrough();
const RaceLookupSchema = z.object({
  id: z.string().uuid(),
}).passthrough();

// Pure, in-memory upsert of one payload's prediction into the predictions.json
// persistence shape. Reproduces what submitPrediction (server/domain/race-prediction-book.ts)
// + predictionsToPersistence together do for a single user, without going through the
// entity/repository:
//   - predictions[userId] and propPicks[userId] are always fully replaced (never merged
//     with whatever was there before)
//   - if the incoming racerIds/propPicks are IDENTICAL to what's already persisted for
//     this user, submittedAt/submittedBy for that user are left untouched — this is what
//     makes deepEqual(current, target) a true no-op detector on re-run
//   - otherwise submittedAt[userId] is set to `now` and submittedBy[userId] is deleted —
//     this backfill originates the write itself (no submitting user), matching
//     predictionsToPersistence's omission of a null submittedBy
function upsertPrediction(
  current: PredictionsPersistence,
  payload: PredictionMutation,
  now: string,
): PredictionsPersistence {
  const { userId, racerIds } = payload;
  const propPicks = payload.propPicks ?? {};

  const existingRacerIds = current.predictions[userId];
  const existingPropPicks = current.propPicks[userId];
  const dataUnchanged =
    existingRacerIds !== undefined &&
    arraysEqual(existingRacerIds, racerIds) &&
    propPicksEqual(existingPropPicks, propPicks);

  const nextSubmittedAt = { ...(current.submittedAt ?? {}) };
  const nextSubmittedBy = { ...(current.submittedBy ?? {}) };

  if (!dataUnchanged) {
    nextSubmittedAt[userId] = now;
    delete nextSubmittedBy[userId];
  }

  return {
    predictions: { ...current.predictions, [userId]: [...racerIds] },
    submittedAt: nextSubmittedAt,
    submittedBy: nextSubmittedBy,
    propPicks: { ...current.propPicks, [userId]: { ...propPicks } },
  };
}

export async function runBackfillPredictions(opts: { dryRun: boolean; confirmed: boolean }): Promise<DatafixStepResult> {
  const shouldWrite = willWrite(opts);
  const reporter = createDatafixReporter(opts);

  // Imported dynamically rather than at module top level: lib/blob/supabase.ts
  // constructs its Supabase client unconditionally at module-evaluation time (even
  // when SUPABASE_URL is unset and LocalBlobStore is what actually gets used), so a
  // static top-level import here would crash on that before parseDatafixArgs/
  // printDatafixBanner above ever get a chance to report a bad flag.
  const { blob } = await import("@/lib/blob");

  const races = loadAllPayloads();
  console.log(`races found: ${races.map((r) => r.title).join(", ") || "(none)"}`);

  // Racers, races (for this motorsport), and leagues are each read once, up front —
  // every payload in this run shares the same motorsport, and none of these three
  // blobs are mutated by this script.
  const racersRaw = await blob.read<unknown>(RACERS_PATH);
  const allRacers = racersRaw === null ? [] : z.array(RacerLookupSchema).parse(racersRaw);
  const racerIdSet = new Set(allRacers.map((racer) => racer.id));

  const racesRaw = await blob.read<unknown>(motorsportRacesPath(MOTORSPORT_ID));
  const allRaces = racesRaw === null ? [] : z.array(RaceLookupSchema).parse(racesRaw);

  const leaguesRaw = await blob.read<unknown>(LEAGUES_PATH);
  const allLeagues = leaguesRaw === null ? [] : z.array(LeagueLookupSchema).parse(leaguesRaw);

  for (const r of races) {
    console.log(`\n${r.title} (${r.payloads.length} payload(s)):`);
    const runId = generateDatafixRunId(`${DATAFIX_ID}-${r.title.toLowerCase()}`);
    try {
      const race = allRaces.find((x) => x.id === r.raceId);
      if (!race) {
        reporter.error(r.title, new Error(`race ${r.raceId} not found under motorsport ${MOTORSPORT_ID}`));
        continue;
      }

      const league = allLeagues.find((x) => x.id === r.leagueId);
      if (!league) {
        reporter.error(r.title, new Error(`league ${r.leagueId} not found`));
        continue;
      }

      const teamsRaw = await blob.read<unknown>(teamsPath(r.leagueId));
      const teams = teamsRaw === null ? [] : z.array(TeamLookupSchema).parse(teamsRaw);
      const memberIdSet = new Set([...league.memberIds, ...teams.flatMap((t) => t.memberIds)]);

      const dataPath = predictionsPath(r.leagueId, r.raceId);
      const raw = await blob.read<unknown>(dataPath);
      const current = raw === null ? EMPTY_PREDICTIONS : PredictionsPersistenceSchema.parse(raw);

      let target = current;

      // Payloads that computed a real change and would be written for this race.
      // reporter.applied() is deferred until AFTER the batched write below actually
      // succeeds — see that block's comment for why.
      const pendingApplied: { label: string; detail: string }[] = [];

      for (const p of r.payloads) {
        const label = `${r.title}: ${p.userId}`;
        try {
          const referencedRacerIds = [
            ...p.racerIds,
            ...Object.entries(p.propPicks ?? {}).filter(([k]) => k !== "fastestPitStop").map(([, v]) => v),
          ];
          const unresolved = referencedRacerIds.filter((id) => !racerIdSet.has(id));
          if (unresolved.length > 0) {
            reporter.error(label, new Error(`unresolved racerId(s): ${unresolved.join(", ")}`));
            continue;
          }
          if (!memberIdSet.has(p.userId)) {
            reporter.error(label, new Error("not found in this league's team/league membership"));
            continue;
          }

          const existed = target.predictions[p.userId] !== undefined;
          const now = new Date().toISOString();
          const candidate = upsertPrediction(target, p, now);
          PredictionsPersistenceSchema.parse(candidate); // validate the computed target shape too

          if (deepEqual(target, candidate)) {
            reporter.noop(label, "already matches");
            continue;
          }

          // Etiquette: never dump the full predictions.json before/after (every user in
          // the league) to the console — just this one payload's shape.
          const detail = `${p.racerIds.length} racer pick(s)${p.propPicks ? `, ${Object.keys(p.propPicks).length} prop pick(s)` : ""}`;
          if (!shouldWrite) {
            reporter.plan(label, existed ? "existing prediction" : "(none)", `${existed ? "overwritten" : "new"} — ${detail}`);
            target = candidate;
            continue;
          }

          pendingApplied.push({ label, detail: `${existed ? "overwritten" : "submitted"} — ${detail}` });
          target = candidate;
        } catch (e) {
          reporter.error(label, e);
        }
      }

      if (deepEqual(current, target)) continue; // nothing changed for this race's predictions.json
      if (!shouldWrite) continue; // per-payload plan() output above already covers this

      // The write for this race's predictions.json happens exactly once, batching every
      // payload's upsert. reporter.applied() must only fire for payloads whose change
      // actually made it to storage — every payload that computed a real change sits in
      // pendingApplied until the write below either succeeds (each reported applied) or
      // fails (each reported as an error instead).
      try {
        await writeDatafixRevertSnapshot(blob, runId, dataPath, current);
        await blob.write(dataPath, target);
      } catch (e) {
        for (const item of pendingApplied) reporter.error(item.label, e);
        continue;
      }

      for (const item of pendingApplied) reporter.applied(item.label, item.detail);
    } catch (e) {
      reporter.error(r.title, e);
    }
  }

  return reporter.result();
}

// ---------------------------------------------------------------------------
// Standalone CLI entry — run via:
//   npx tsx scripts/datafix/backfill-predictions/backfill-predictions.ts --dry-run
//   npx tsx scripts/datafix/backfill-predictions/backfill-predictions.ts --yes
// (or via the "Data Fix Runner" GitHub Actions workflow, script_path =
// backfill-predictions/backfill-predictions.ts)
// ---------------------------------------------------------------------------

if (isMainModule(import.meta.url)) {
  const opts: DatafixOpts = parseDatafixArgs(process.argv.slice(2));
  printDatafixBanner(opts, [`target motorsport: ${MOTORSPORT_ID}`]);

  runBackfillPredictions(opts)
    .then((result) => {
      printStepSummary("backfill-predictions", result, opts);
      if (result.failed > 0) process.exitCode = 1;
    })
    .catch((e) => {
      console.error("backfill-predictions: unhandled error", e);
      process.exitCode = 1;
    });
}
